USE ECOMMERCE;

CREATE TABLE CUSTOMERS (CUSTOMER_ID VARCHAR(23), NAME CHAR(23), CITY CHAR(23), JOIN_DATE VARCHAR(23));
CREATE TABLE PRODUCTS (PRODUCT_ID VARCHAR(23), CATEGORY CHAR(23), PRODUCT_NAME CHAR(23), PRICE INT, COST INT);
CREATE TABLE ORDERS (ORDER_ID VARCHAR(23), CUSTOMER_ID VARCHAR(23), PRODUCT_ID VARCHAR(23), ORDER_DATE VARCHAR(23), QUANTITY INT, TOTAL_AMOUNT INT);
CREATE TABLE RETURNS (RETURN_ID VARCHAR(23), ORDER_ID VARCHAR(23), RETURN_DATE VARCHAR(23), REASON CHAR(250));



